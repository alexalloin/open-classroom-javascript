var numero = Number(prompt("Veuillez entrer un numero correspondant a un mois"));
switch (numero) {
    case 1:
        console.log("Il s'agit du mois de janvier");
        break;

    case 2:
        console.log("Il s'agit du mois de fevrier");
        break;

    case 3:
        console.log("Il s'agit du mois de mars");
        break;

    case 4:
        console.log("il s'agit du mois d'avril");
        break;

    case 5:
        console.log("Il s'agit du mois de mai");
        break;

    case 6:
        console.log("Il s'agit du mois de juin");
        break;

    case 7:
        console.log("Il s'agit du mois de juillet");
        break;

    case 8:
        console.log("Il s'agit du mois d'aout");
        break;

    case 9:
        console.log("Il s'agit du mois de septembre");
        break;

    case 10:
        console.log("Il s'agit du mois d'octobre");
        break;

    case 11:
        console.log("Il s'agit du mois de novembre");
        break;

    case 12:
        console.log("Il s'agit du mois de decembre");
        break;

    default:
        console.log("Veuillez choisir une valeur de 1 a 12");
}